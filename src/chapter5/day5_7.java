package chapter5;

public class day5_7
{
    public static void main(String[] args)
    {
        //输出小写的a-z
        for(int i=97;i<=122;i++)
        {
            System.out.print((char)i+" ");
        }
        // 在这里添加换行
        System.out.println();
        //输出大写的A-Z
        for(int i=65;i<=90;i++)
        {
            System.out.print((char)i+" ");
        }
    }
}